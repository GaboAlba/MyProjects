public class Cat {

    // You can define the attributes by declaring them 
    String name ;
    int age ;

    // You can create simple methods
    public void meow() {
        System.out.println("Meow");
    }

    // A static method means that it doesn't need an object to be called upon
    // it is sufficient to call the class
    public static void dingDong(){
        System.out.println("Ding Dong");
    }
    
}
